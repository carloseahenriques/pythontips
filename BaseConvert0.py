# Converte o número 253 base 10 para Hexadecimal (maiúsculo)
print("{0:X}".format(253))

# Converte o número 253 base 10 para Hexadecimal (minúsculo)
print("{0:x}".format(253))

#Converte hexadecimal para decimal
print("{0:10}".format(0x2fd4))

#Converte hexadecimal para binário
print ("{0:b}".format(0xdf))
